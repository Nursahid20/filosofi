             <h3 style="font-weight: bolder;">1. Struktur Keluarga</h3>
             <br/>
             <h4>a. Patrilineal. Keluarga yang dihubungkan atau disusun melalui jalur garis ayah, suku-suku di Indonesia rerata menggunkan struktur keluarga patrilineal.</h4>
             <br/>
             <h4>b. Matrilineal. Keluarga yang dihubungkan atau disusun melalui jalur garis Ibu, Suku Padang merupakan salah satu suku yang masih konsisten menggunakan pola Matrilineal.</h4>
             <br/>
             <h4>c. Matrilokal. Menentukan bahwa keberadaan tempat tinggal satu keluarga (suami istri) yang tinggal bersama keluarga sedarah istri.</h4>
             <br/>
             <h4>d. Patrilokal. Menentukan bahwa keberadaan tempat tinggal satu keluarga (suami istri) yang tinggal bersama keluarga sedarah suami</h4>
             <br/><br/>
             <h3 style="font-weight: bolder;">2. Bentuk Keluarga</h3>
             <br/>
             <h4>Menurut Sussman (1974) dan Maclin (1988).</h4>
             <br/>
             <h4>a. Keluarga Tradisional</h4>
             <br/>
             <h4> 1) Keluarga Inti. Keluarga yang terdiri atas ayah, ibu dan anak.</h4>
             <h4>2) Pasangan Inti. Suami dan istri saja.</h4>
             <h4>3) Keluarga dengan orang tua tunggal. Satu orang yang mengepalai keluarga sebagai konsekuensi perceraian.</h4>
             <h4>4) Bujangan yang tinggal sendirian.</h4>
             <h4>5) Keluarga Besar 3 Generasi (keluarga no. 1,2 dan 3 yang hidup satu rumah).</h4>
             <h4>6) Pasangan Usia Pertengahan atau Lansia. Suami sebagai pencari nafkah, istri tinggal dirumah.</h4>
             <h4>7) Jaringan Keluarga Besar. Dua keluarga inti atau lebih dari kerabat primer atau anggota keluarga yang tidak menikah hidup berdekatan dalam daerah geografis dan dalam system resiprok atau tukar menukar barang dan jasa.</h4>
             <br/>
             <h3>b. Keluarga Non Tradisional</h3>
             <br/>
             <h4>1) Keluarga dengan orang tua beranak tanpa menikah.</h4>
             <h4>2) Pasangan yang memiliki anak tanpa menikah.</h4>
             <h4>3) Pasangan kumpul kebo. Pasangan yang hidup bersama tanpa menikah.</h4>
             <h4>4) Keluarga Gay/Lesbi, orang yang berjenis kelamin sama yang hidup bersama sebagai pasangan yang menikah.</h4>
             <h4>5) Keluarga Komuni, rumah tangga yang terdiri dari lebih dari satu pasangan monogamy dengan anak-anak, secara bersama-sama menggunakan fasilitas, sumber-sumber dan memili pengalaman yang sama.</h4>
             <br/>
             <h3 style="font-weight: bolder;">Keluarga menurut Anderson Carter :</h3>
             <h4>1) Keluarga Inti, keluarga yang terdiri dari ayah, ibu dan anak-anak.</h4>
             <h4>2) Keluarga Besar, keluarga inti ditambah dengan sanak saudara ; nenek, kakek, keponakan, sepupu, paman, bibi dan sebagainya.</h4>
             <h4>3) Keluarga Berantai, keluarga yang terdiri dari wanita dan pria yang menikah lebih dari satu kali dan merupakan keluarga inti.</h4>
             <h4>4) Keluarga duda/janda, keluarga yang terjadi karena perceraian atau kematian.</h4>
             <h4>5) Keluarga Komposisi, Keluarga yang perkawinannya berpoligami dan hidup secara bersama.</h4>
             <h4>6) Keluarga Kabitas. Keluarga yang menjadi satu tanpa pernikahan tetapi membentuk satu keluarga.</h4>
             <br/><br/>
                <h3 style="font-weight: bolder;">3. Fungsi Keluarga</h3>
             <br/>
             <h3>a. Fungsi Biologis.</h3>
             <h4>Fungsi biologis keluarga bukan hanya ditujukan untuk meneruskan kelangsungan keturunan, tetapi juga memelihara dan membesarkan anak dengan gizi seimbang, memelihara dan merawat anggota keluarga juga bagian dari fungsi biologis keluarga.</h4>
             <br/>
             <h3>b. Fungsi Psikologis.</h3>
             <h4>Keluarga menjalankan fungsi psikologisnya antara lain untuk memberikan kasih saying dan rasa aman, memberikan perhatian antara anggota keluarga, membina pendewasaan keprobadian anggota keluarga, memberikan identitas keluarga.</h4>
             <br/>
             <h3>c. Fungsi Sosial.</h3>
             <h4>Fungsi social tercermin untuk membina sosialisasi pada anak, membentuk nilai dan norma yang diyakini anak, memberikan batasan-batasan perilaku yang boleh dan tidak boleh pada anak. Meneruskan nilai nilai budaya.</h4>
             <br/>
             <h3>d. Fungsi Ekonomi</h3>
             <h4>Keluarga menjalankan fungsi ekonominya untuk mencari sumber-sumber penghasilan untuk memenuhi kebutuhan keluarga, mengaturan penggunaan penghasilan keluarga untuk memenuhi kebutuhan keluarga, menabung untuk memenuhi kebutuhan ke butuhan yang akan datang, misalanya pendidikan anak anak, jaminan hari tua dan sebagainya.</h4>
             </br>
             <h4>Fungsi ekonomi ini, secara kultural di negera-negera Asia di pegang teguh oleh kepala keluarga yaitu suami, tetapi lambat laun nilai ini akan memudar, tetapi lambatlaun nilai itu akan memudar, banyak wanita sebagai single parent memenuhi fungsi ekonomi.</h4>
             <br/>
             <h3>e. Fungsi Pendidikan</h3>
             <h4>Keluarga menjalankan fungsi pendidikan untuk menyekolahkan anak dalam rangka memberikan pengetahuan,keterampialan dan dalam rangka memeberikan pengetahuan, keterampilan dan dalam membentuk prilaku anak, mempersiapkan anak dalam kehiupan dewasa, mendidik anak sesuai dengan tingkatan perkembangan.</h4>
             <br/><br/>
                <h3 style="font-weight: bolder;">4. Tahapan Kehidupan Keluarga</h3>
             <br/>
             <h4>a. Keluarga pemula atau pasangan baru menikah
             Tahapan ini dimulai saat dua insan dewasa mengikat janji pernikahan dengan landasan cinta dan kasih sayang. Tugas pada tahapan ini selaim memuaskan antar pasangan, juga beradaptasi dengan keluarga besar dari masing-masing pihak, tak lupa pasangan baru ini akan merencanakan jumlah anak.</h4>
             <br/>
             <h4>b. Keluarga dengan menunggu kelahiran anak
             Tahapan kehidupan keluarga selanjutnya adalah keluarga dimana suami dan istri menunggu kelahiran anak sebagai tugas melangsungkan keturunan. Tugas keluarga pada tahapan ini adalah mempersiapkan kebutuhan anak yang dilahirkan seperti halnya, kebutuhan baju dan celana anak, kebutuhan, kebutuhan ruang khusus untuk anak kebutuhan biaya melahirkan dsb.</h4>
             <br/>
             <h4>c. Keluarga dengan mempunyai bayi
             Keluarga dengan mengasuh bayi, mendidik dan mengasuh bayi dengan penuh kasih sayang, mengingat pada tahapan ini kebutuhan bayi sepenuhnya tergantung dari kedua orang tuanya</h4>
             <br/>
             <h4>d. Keluaga dengan anak usia prasekolah .
             Tahapan perkembangan keluarga dengan anak usia prasekolah, anak mulai tersosialisasi, bergaul dengan teman sebaya, tetapi sangat rawan terkena masalah kesehatan. Tugas keluarga terhadap anak usia prasekolah adalah menanamkan nilai-nilai kehidupan, agama dan sosial budaya.</h4>
             <br/>
             <h4>e. Keluarga dengan anak usia sekolah.
             Dalam tahapan ini, tugas keluarga adalah mendidik anak, mempersiapkan anak untuk masa depannya, membiasakan anak belajar teratur, menyelesaikan tugas-tugas sekolahnya, memberikan pengertian pada anak tentang pentingnya belajar pada anak-anak.</h4>
             <br/>
             <h4>f. Keluarga dengan anak remaja.
             Keluarga dengan anak remaja berada pada posisi dilematis, mengingat anak sudah menurun perhatiannya terhadap orang tua dibandingkan dengan teman sebayanya. Untuk itu orang tua harus memberikan contoh yang baik dan mampu mendekati remaja dengan cara yang dapat diterima mereka. Pada masa ini banyak ditemukan friksi antara anak remaja dengan orang tuanya.</h4>
             <br/>
             <h4>g. Keluarga dengan melepaskan anak ke masyarakat.
             Setelah menyelesaikan masa remajanya, maka keluarga harus merelakan anak-anaknya meninggalkan rumah orang tuanya untuk memulai hidup baru untuk bekerja dan memulai hidup baru.</h4>
             <br/>
             <h4>h. Keluarga dengan tahapan berdua kembali.
             Anak-anak yang sudah berkeluarga tentunya memulai kehidupan baru dengan tinggal terpisah dengan kedua orang tuanya, sedangkan orang tuanya akan kembali kepada siklus kehidupan dimana mereka akan berdua kembali dalam menempuh kehidupan selanjutnya.</h4>
             <br/>
             <h4>i. Keluarga dengan tahapan masa tua.
             Keluarga dengan tahapan lansia akan mengalami kesendirian, kesepian dan mencoba untuk melalui hari-harinya dengan menyibukkan diri dengan berkebun, berolahraga atau bahkan menimang cucu. Tetapi pada dasarnya masa tua akan mempersiapkan diri untuk meninggalkan kehidupan ini.</h4>
             <br/><br/>
             <h3 style="font-weight: bolder;">5. Faktor-faktor yang mempengaruhi keluarga</h3>
             <br/>
             <h3>a. Faktor Fisik</h3>
             <h4>Menurut riset (Ross, Mirowsaky dan Goldstei 1990) memberikan penekanan bahwa ada hubungan yang positif antara perkawinan dengan kesehatan fisik. Pernikahan diartikan dengan menggabungkan kekuatan dari dua sumber (istri dan suami), contoh seorang suami yang sebelum menikah terlihat kurus maka beberapa bulan setelah menikah suami tersebut terlihat gemuk, beberapa alas an dikemukakan bahwa dengan menikah suami akan diurus dan diperhatikan kebutuhannya begitupun pada istri.</h4>
             <br/>
             <h3>b. Faktor Psikis.</h3>
             <h4>Terbentuknya keluarga akan memberikan danpak psikologis yang besar, perasaan nyaman karena saling memperhatikan, saling memberikan penguatan atau dukungan. Suami akan merasa tentram dan terarah setelah beristri begitupun sebaliknya. Tetapi beberapa hasil riset menunjukkan peningkatan kecemasan pada istri setelah menikah, hal ini dimungkinkan karena bertambahnya beban yang dialami istri setelah bersuami.</h4>
             <br/>
             <h3>c. Faktor Sosial.</h3>
             <h4>Status sosial memiliki danfak yang signifikan terhadap fungsi kesehatan sebuah keluarga. Dalam sebuah keluarga ada kecenderungan semakin tinggi pendapatan yang diterima semakin baik taraf kehidupannya, tingginya pendapatan yang diterima akan berakses pada pemahaman tentang pentingnya kesehatan, jenis pelayanan kesehatan yang akan dipilih dan bagaimana berspon pada penyakit.</h4>
             <br/>
             <h3>d. Faktor Budaya.</h3>
             <br/>
             <h4>1) Keyakinan dan praktek kesehatan.
             Setiap suku atau bangsa memiliki keyakinan dan penilaian yang berbeda-beda terhadap fungsi kesehatan. Keyakinan keluarga terhadap fungsi kesehatan yang dibawah sebelumnya.</h4>
             <br/>
             <h4>2) Nilai-nilai Keluarga.
             Nilai-nilai yang dimiliki oleh keluarga mempengaruhi kesehatan keluarga yang bersangkutan, misal sebuah keluarga yang kurang memperdulikan kesehatan dan merasa tanpa melakukan upaya apapun, kesehatan keluarganya terjaga. Tetapi keluarga tersebut akan mengalami kesulitan jika suatu waktu nilai yang diyakininya ternyata salah dan terbukti bahwa kesehatan keluarganya terganggu.</h4>
             <br/>
             <h4>3) Peran, Kekuatan dan Pola Komunikasi Keluarga.
             Dampak budaya pada peran, kekuatan dan komunikasi keluarga berbeda-beda pada setiap keluarga. Jika terjadi perubahan pada budaya dengan semestinya terjadi pergeseran peran, aturan-aturan, kekuatan dan pola komunikasi.</h4>
             <br/>
             <h4>4) Koping Keluarga.
             Koping keluarga dipengaruhi oleh budaya, keluarga akan berusaha beradaptasi dengan perubahan budaya. Koping diartikan sebagai respon positif baik kognitif, efektif ataupun psikomotor bagi keluarga dalam menyelesaikan masalah yang terjadi pada keluarga.</h4>
             </br>
             <h3 style="font-weight: bolder;">6. Ukuran Keluarga Sejahtera di Indonesia.</h3>
             <h4>Untuk mencapai cita-cita luhur bangsa Indonesia, yaitu mensejahterakan rakyatnya, maka pemerintah menetapkan undang-undang sebagai pedoman :</h4>
             <br/>
             <h4>a. Keputusan Presiden RI Nomor 8 TAhun 1970, dibentuk BKKBN untuk mencapai NKKBS.</h4>
             <br/>
             <h4>b. Undang-Undang Nomor 10 Tahun 1982, menetapkan gerakan KB menjadi gerakan pembangunan keluarga sejahtera.</h4>
             <br/>
             <h4>c. Pasal 4 Undang-Undang Nomor 12 Tahun1992, Tujuan pembangunan keluarga sejahtera adalan untuk mengembangkan kualitas keluarga agar dapat timbul rasa aman, tentram dan harapan masa depan yang lebih baik dalam mewujudkan kesehjahteraan lahir dan kebahagiaan batin.</h4>
             <br/>
             <h3 style="font-weight: bolder;">7. Pengertian-pengertian yang berkaitan dengan keluarga sejahtera :</h3>
             </br>
             <h4>a. Keluarga sejahtera adalah keluarga yang dibentuk berdasarkan atas perkawinan yang sah, mampu memenuhi kebutuhan hidup spiritual dan material yang layak, bertakwa kepada Tuhan Yang Maha Esa, memiliki hubungan yang sama dan seimbang antara anggota dengan masyarakat dan lingkungannya.</h4>
             </br>
             <h4>b. Keluarga Berencana adalah upaya peningkatan kepedulian peran serta masyarakat melalui pendewasaan usia perkawinan, pengaturan kelahiran, pembinaan ketahan keluarga, peningkatan kesejahteraan keluarga untuk mewujudkan keluarga kecil, bahagia dan sejahtera.</h4>
             </br>
             <h4>c. Kualitas keluarga adalah kondisi keluarga yang mencakup aspek pendidikan, kesehatan ekonomi, sosial budaya, kemandirian keluarga dan mental spiritual nilai-nilai agama yang merupakan dasar untuk mencapai keluarga sejahtera.</h4>
             </br></br>
             <h3 style="font-weight: bolder;">Tahapan Keluarga Sejahtera.</h3>
             <h4>Untuk mengukur keberadaan keluarga menurut tingkat kesejahteaannya dikembangkan 23 indikator operasional yang menggambarkan tingkat pemenuhan kebutuhan dasar keluarga, kebutuhan sosial psikologis, kebutuhan pengembangan. Adapun rinciannya sebagai berikut :</h4>
             </br>
             <h4>1. Keluarga Pra Sejahtera.</h4>
             <h4>Keluarga-keluarga yang belum dapat memenuhi kebutuhan dasar secara minimal, seperti kebutuhan akan pengajaran, agama, pangan, sandang dan kesehatan.</h4>
             </br>
             <h4>2. Keluarga Sejahtera Tahap I.</h4>
             <h4>Keluarga-keluarga yang dapat memenuhi kebutuhan dasarnya secara minimal, tetapi belum dapat memenuhi keseluruhan kebutuhan sosial psikologis seperti kebutuhan akan pendidikan, keluarga berencana, interaksi dalam keluarga, interaksi dengan lingkungan tempat tinggal dan transportasi.</h4>
             </br>
             <h4>3. Keluarga Sejahtera Tahap II.</h4>
             <h4>Keluarga-keluarga yang disamping dapat memenuhi kebutuhan dasarnya, juga telah dapat memenuhi seluruh kebutuhan secara psikologisnya, akan tetapi belum dapat memenuhi keseluruhan kebutuhan perkembangan seperti kebutuhan untuk menabung dan memperoleh informasi.</h4>
             </br>
             <h4>4. Keluarga Sejahtera Tahap III.</h4>
             <h4>Keluarga-keluarga yang telah dapat memenuhi kebutuhan dasarnya, juga telah dapat memenuhi seluruh kebutuhan secara psikologisnya dan kebutuhan pengembangannya, namun belum dapat memberikan sumbangan yang maksimal terhadap masyarakat lingkungannya.</h4>
             </br>
             <h4>5. Keluarga Sejahtera Tahap IV.</h4>
             <h4>Keluarga-keluarga yang telah dapat memenuhi seluruh kebutuhannya, baik yang bersifat dasar, sosial psikologis, maupun yang bersifat pengembangan serta telah dapat pula memberikan sumbangan nyata dan berkelanjutan bagi masyrakat lingkungannya.</h4>
             </br></br>
